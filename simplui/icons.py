_icon = None
_theme = None
def icon_theme(theme):        
    global _icon
    global _theme
    
    if _icon and theme == _theme:
        return _icon
    
    _theme = theme
    _icon = theme.copy()
    for key in  theme.keys():
        new_key = key
        if 'font' in key:
            new_key = key.replace('font', 'icon')
        _icon[key] = theme[new_key]
    return _icon 

